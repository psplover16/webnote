<template>
  <div>
    <h2 @click="isShow = !isShow">
      {{ title }}
      <div :class="['isShow', isShow ? '' : 'rotate180']">^</div>
    </h2>
    <ol type="I" v-if="isShow">
      <li>
        title屬性，hover會出現提示
        <div title="hover出現提示">{{ titleAttr }}</div>
      </li>
    </ol>
  </div>
</template>

<script setup>
import { ref } from "vue";
defineProps({
  title: String,
});
const isShow = ref(false);
const titleAttr = `<span title="hover出現提示">hover出現提示</span>`;
</script>
